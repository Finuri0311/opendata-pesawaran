    public static function getLatestArticles($limit = 3)
    {
        $articles = self::getAllArticles();
        return array_slice($articles, 0, $limit);
    }

    public static function getArticleById($id)
    {
        $articles = self::getAllArticles();
        return collect($articles)->firstWhere('id', $id);
    }

    public static function getFilteredArticles($category = null, $search = null)
    {
        $articles = self::getAllArticles();
        
        if ($category) {
            $articles = array_filter($articles, function($article) use ($category) {
                return $article['category'] === $category;
            });
        }
        
        if ($search) {
            $articles = array_filter($articles, function($article) use ($search) {
                return stripos($article['title'], $search) !== false || 
                       stripos($article['description'], $search) !== false ||
                       stripos($article['content'], $search) !== false;
            });
        }
        
        return array_values($articles);
    }
